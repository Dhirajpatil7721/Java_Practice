// Check whether a character is a alphabet or not

public class Alphabet_or_Not {
    public static void main(String[] args) {
        char str = '7';

        if ((str >= 'a' && str <= 'z') || (str >= 'A' && str <= 'Z')) {
            System.out.println("Yes...! It is Alphabet ");

        } else {
            System.out.println("No...*");

        }
    }
}
